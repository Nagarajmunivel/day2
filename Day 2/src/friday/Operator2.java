package friday;
//Relational operators
public class Operator2 {
public static void main(String args[])
{
	int a=10,b=20;
	System.out.println(a+">"+b+" "+ (a>b));
	System.out.println(a+"<"+b+" "+(a<b));
	System.out.println();
	int c=30,d=20,e=20;
	System.out.println(c+">="+d+" "+ (c>=d));
	System.out.println(d+"<="+e+" "+ (d<=e));
	System.out.println();
	System.out.println(d+"!="+e+" "+ (d!=e));
	
}
}
